class Programmar:
    company = "Microsoft"
    def __init__(self, name, salary, pin):
        self.name = name
        self.salary = salary
        self.pin = pin 



p = Programmar("Harry", 12000, 102)
print(p.name, p.salary, p.pin, p.company)

p = Programmar("mohid", 10000, 9)
print(p.name, p.salary, p.pin, p.company)

